async function run(db) {
  console.log('  Expanding SIC codes to full EDGAR list...');

  let added = 0;
  let skipped = 0;

  for (const sic of FULL_SIC_LIST) {
    const exists = await db('sic_codes').where('sic_code', sic.sic_code).first();
    if (exists) { skipped++; continue; }

    await db('sic_codes').insert({
      sic_code:     sic.sic_code,
      division:     sic.division || null,
      major_group:  sic.sic_code.substring(0, 2),
      name:         sic.name,
      name_fr:      null,
      description:  null,
      entity_count: 0,
    });
    added++;
  }

  console.log(`  ✓ SIC expansion: ${added} added, ${skipped} already existed`);
}

module.exports = { run };
